    private void prencheTabela() {
        controle.ControleFornecedor controleF = new ControleFornecedor();
        DefaultTableModel modelo = (DefaultTableModel) this.tabelaFornecedores.getModel();
        try {
            fdto = controleF.read();
            String[] dados = new String[4];
            for(FornecedorDTO f : fdto.getFornecedoresRegistrados()){
                dados[0] = f.getNome();
                dados[1] = f.getCnpj();
                dados[2] = f.getTelefone();
                dados[3] = f.getIdRegistro()+"";
                modelo.addRow(dados);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }

    }
